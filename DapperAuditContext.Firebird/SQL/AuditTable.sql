CREATE TABLE AUDITTABLE (
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	USERNAME VARCHAR(255) NOT NULL,
	KEYFIELDID INTEGER NOT NULL,
	ACTIONTYPE INTEGER NOT NULL,
	DATETIMESTAMP TIMESTAMP NOT NULL,
	DATAMODEL VARCHAR(255) NOT NULL,
	CHANGES BLOB SUB_TYPE TEXT NOT NULL,
	VALUEBEFORE BLOB SUB_TYPE TEXT NOT NULL,
	VALUEAFTER BLOB SUB_TYPE TEXT NOT NULL,
	CONSTRAINT AUDITTABLE_PK PRIMARY KEY (ID)
);
